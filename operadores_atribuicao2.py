saldo = 200
print(saldo)

#adicionando
saldo += 200
print(saldo)

#subtraindo
saldo -=100
print(saldo)

#Multiplicação
saldo *=10
print(saldo)
#divisão
saldo /=10
print(saldo)
#divisao - retorna inteiro
saldo //=100
print(saldo)
#retorna o resto mod 10
saldo %=10
print(saldo)
